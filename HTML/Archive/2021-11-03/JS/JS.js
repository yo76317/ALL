// 多筆變數 //
let a="1";
let b="2";
let c="3";
console.log(a);
console.log(b);
console.log(c);

// let 宣告變數，數值可改變 //
let num1 = 100;
console.log("num1 = " + num1);
num1 = 200;
console.log("num1 = " + num1);

// const 固定改變值，宣告一次，之後數值無法改變 //
const num2 = 100;
console.log("num2 = " + num2);
num2 = 200;
console.log("num2 = " + num2);


